% Заданий інтервал [0; 2п].
x = 0:0.01:2*pi;
% В умові, мабуть, помилка "2п2\pi2п", тому я взяв просто 2п.

% Обчислення значень заданих функцій.
f1 = sin(x.^2);
f2 = cos(x.^2);
% В умові x2, що я зрозумів як x до степеню 2.

% Зображення графіків (схоже на matplotlib у Python)
% Два графіка на одній фігурі: один червоний (r), другий синій (b),
plot(x, f1, 'r', x, f2, 'b'),
% що мають легенду (підписи у кутку)
legend('sin(x^2)', 'cos(x^2)'),
% та сітку.
grid on;
