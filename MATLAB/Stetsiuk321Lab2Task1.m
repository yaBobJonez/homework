clear

t=1:4; % задається кількість відліків
Y=[7, 5, 1, 12]; % визначається вектор-стовпець Y

M=[t(1)^3, t(1)^2, t(1), 1; % розрахунок матриці М
t(2)^3, t(2)^2, t(2), 1;
t(3)^3, t(3)^2, t(3), 1;
t(4)^3, t(4)^2, t(4), 1];

X=M^(-1)*Y'; % розраховуємо значення вектора-стовпця Х

t2=1:0.01:4;
P=X(1)*t2.^3+X(2)*t2.^2+X(3)*t2+X(4); % будуємо поліном

plot(t, Y, '*', t2, P), grid % вивід графіка на екран
