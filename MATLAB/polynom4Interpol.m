function [t2,P] = polynom4Interpol(t,Y)
    M=[t(1)^4, t(1)^3, t(1)^2, t(1), 1; % розрахунок матриці М
       t(2)^4, t(2)^3, t(2)^2, t(2), 1;
       t(3)^4, t(3)^3, t(3)^2, t(3), 1;
       t(4)^4, t(4)^3, t(4)^2, t(4), 1
       t(5)^4, t(5)^3, t(5)^2, t(5), 1];
    
    % аналізатор коду порадив лівостороннє матричне ділення inv(M)*Y'
    X=M\Y'; % розраховуємо значення вектора-стовпця Х
    
    t2=1:0.01:5;
    P = X(1)*t2.^4 + X(2)*t2.^3 + X(3)*t2.^2 + X(4)*t2 + X(5); % будуємо поліном
end
